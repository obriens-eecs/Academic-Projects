PtGt = 1;
Gr = 1;
L = 1;
d = 1:1:100000;
fc1 = 9e8;
fc2 = 2e9;
c = 3e8;
lambda1 = c / fc1;
lambda2 = c / fc2;
Pr1 = (lambda1 ./ (4 * pi * d) ).^2 * PtGt * Gr / L;  
PrL1 = log10(Pr1*10000);
Pr2 = (lambda2 ./ (4 * pi * d) ) .^2 * PtGt * Gr / L;
PrL2 = log10(Pr2*10000);
l = log10(d);
subplot(2,1,1);
plot(l,PrL1);
title("Recieved Power for 900MHz");
ylabel("Recieved Signal Power (dB)");
xlabel("Logarithmic Scale of Distance");
subplot(2,1,2);
plot(l,PrL2);
title("Recieved Power for 2GHz");
ylabel("Recieved Signal Power (dB)");
xlabel("Logarithmic Scale of Distance");
